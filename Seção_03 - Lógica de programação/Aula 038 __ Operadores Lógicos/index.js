/*
OPERADORES LÓGICOS
&&  -  AND  -  E
||  -  OR   -  OU
!   -  NOT  -  NEGAÇÃO
*/

//And
const expressaoAND = true && true && true //Para true, todas precisam ser true
console.log(expressaoAND)


//OR
const expressaoOR = true || false || false //Para true, uma precisa ser true
console.log(expressaoOR)


//NOT
console.log(!false) //Inverte a expressão
