/*
- Escopo léxico refere-se à região do código, na qual uma determinada variável é visível e pode ser acessada. 
- O escopo determina onde uma variável pode ser usada e reconhecida pelo compilador. 
- O escopo define a área onde uma variável é válida e pode armazenar ou recuperar informações. 
- Variáveis definidas em um escopo específico não são visíveis ou acessíveis em outros escopos, a menos que haja um mecanismo explícito de compartilhamento ou passagem de variáveis entre esses escopos. 
- Isso ajuda a evitar conflitos e ambiguidades, garantindo que diferentes partes de um programa possam usar nomes de variáveis sem interferir umas nas outras.
*/