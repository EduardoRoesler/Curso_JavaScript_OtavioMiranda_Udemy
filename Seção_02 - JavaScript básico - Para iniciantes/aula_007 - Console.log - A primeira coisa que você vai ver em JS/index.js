//Aspas simples e duplas
console.log('Aspas simples')
console.log("Aspas duplas")

//Aspas dentro de aspas
console.log("'Aspas' que aparecem no texto") //Faço uso de aspas intercaladas
console.log('"Aspas" que aparecem no texto') //Independente de qual está dentro ou fora

//Crases
console.log(`Crases`) //Usado para template Strings
console.log(`"Cra" 'ses'`) //Por se tratar de uma template string, pode-se usar aspas livremente dentro das crases

//Números
console.log(25) //por se tratarem de number, podem ser postos livremente dentro do log
console.log(25.55) //Usa-se ponto para separar casas decimais

//Virgula
console.log('Mantém', 'o', 'texto', 'inline', 'no console')//Atua como separador e tem propriedade de manter inline o console.log

